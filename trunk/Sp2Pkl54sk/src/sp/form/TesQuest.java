    private void registerCard() {
        clc = getClc();
        clc.setCardLayout((CardLayout) fixedQuest1.getLayout());
        clc.setParentCard(fixedQuest1);

        FormControl.init(fixedQuest1, clc);
        endPos += fixedQuest1.getHal12().getMaxPertanyaan() - 1;
        limitSection.add(0, endPos);
        endPos += fixedQuest1.getHal21().getMaxPertanyaan();
        limitSection.add(1, endPos);
        endPos += fixedQuest1.getHal31().getMaxPertanyaan();
        limitSection.add(2, endPos);
        endPos += fixedQuest1.getHal41().getMaxPertanyaan();
        limitSection.add(3, endPos);
        endPos += fixedQuest1.getHal51().getMaxPertanyaan();
        limitSection.add(4, endPos);
        endPos += fixedQuest1.getHal61().getMaxPertanyaan();
        limitSection.add(5, endPos);
        FormControl.setLimitSection(limitSection);
    }

