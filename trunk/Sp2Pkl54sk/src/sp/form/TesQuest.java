    private void setScrollListener(JPanel aThis) {
        KeyboardFocusManager.getCurrentKeyboardFocusManager().
                addPropertyChangeListener("focusOwner", new PropertyChangeListener() {

                    @Override
                    public void propertyChange(PropertyChangeEvent evt) {

                        if (!(evt.getNewValue() instanceof JComponent)) {
                            return;
                        }

                        JViewport viewport = (JViewport) aThis.getParent();
                        JComponent focused = (JComponent) evt.getNewValue();
                        if (aThis.isAncestorOf(focused)) {
                            Rectangle rect = focused.getBounds();
                            Rectangle r2 = viewport.getVisibleRect();
                            //System.out.println(aThis);
                            aThis.scrollRectToVisible(new Rectangle(rect.x, rect.y - 500, (int) r2.getWidth(), (int) r2.getHeight()));
                        }
                    }
                });
    }

    private void newDoc() {
        fq = getFixedQuest1();
        b1 = new B1();
        b2 = new B2();
        b3 = new B3();
        b4 = new B4();
        ac = new SaveController(b1, b2, b3, b4,
                fq.getHal12(),
                fq.getHal21(),
                fq.getHal31(),
                fq.getHal41(),
                fq.getHal51(),
                fq.getHal61(),
                this);
        saveBtn.addActionListener(ac);
    }
